package jp.ac.chibafjb.x15g022.kd135;

public class Main {

	public static void main(String[] args) {
		int a;    //変数aを定義
		int b;    //変数bを定義
		String c; //変数cを定義
		a = 10; //右辺値10を左辺値aへ入れる
		b = 20; //右辺値10を左辺値bへ入れる
		c = "abcdef"; //右辺値「abcdef」を左辺値cへ入れる

		System.out.println("a=" + a);
		System.out.println("b=" + b);
		System.out.println("c=" + c);

	}

}
